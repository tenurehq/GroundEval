@runtime_checkable
class CorpusAdapter(Protocol):
    """
    Provides artifact retrieval for the gated tool layer.

    Two built-in implementations are provided:
      - FileCorpusAdapter   — artifacts are files in a directory
      - NullCorpusAdapter   — context-injection mode, no retrieval needed

    Implement this protocol to plug in MongoDB, Elasticsearch, etc.
    """

    def fetch(self, artifact_id: str, as_of: str | None = None) -> dict | None:
        """Return artifact dict or None if not found / gated by timestamp."""
        ...

    def search(
        self,
        query: str,
        artifact_type: str | None = None,
        as_of: str | None = None,
        limit: int = 10,
    ) -> list[dict]:
        """Full-text or keyword search. Return list of artifact dicts."""
        ...

    def timestamp_of(self, artifact_id: str) -> str | None:
        """Return ISO timestamp of artifact creation, or None."""
        ...

    def subsystem_of(self, artifact_id: str) -> str | None:
        """Return subsystem name for this artifact (e.g. 'email', 'jira')."""
        ...

    def list_ids(self, subsystem: str | None = None) -> list[str]:
        """List all artifact IDs, optionally filtered by subsystem."""
        ...


@runtime_checkable
class AccessPolicy(Protocol):
    """
    Declares who can see what.

    The simplest implementation reads from config.yaml:

        roles:
          engineer:
            subsystems: [jira, git, slack, confluence]
          sales:
            subsystems: [salesforce, email, slack]

    Implement this protocol for more dynamic rules (e.g. per-project access).
    """

    def subsystems_for_role(self, role: str) -> set[str]:
        """Return set of subsystem names accessible to this role."""
        ...

    def role_for_actor(self, actor_id: str) -> str | None:
        """Return the role name for this actor, or None if unknown."""
        ...

    def visible_artifacts(
        self,
        actor_id: str,
        all_artifact_ids: list[str],
        as_of: str | None = None,
        corpus: CorpusAdapter | None = None,
    ) -> set[str]:
        """
        Return the subset of artifact IDs visible to this actor.

        Default implementation: all artifacts whose subsystem is in the
        actor's role subsystem set. Override for finer-grained control
        (e.g. direct-involvement tracking from an event log).
        """
        ...


@dataclass
class CausalJoinSpec:
    """
    A single join condition for linking a cause event to an effect event.

    Fields are dotted paths into the event dictionaries:
        cause:  "artifact_ids.jira"
        effect: "facts.source_ticket"

    A CausalLinkSpec may contain a list of these; ALL must match for a
    candidate effect to be accepted.
    """

    cause: str
    effect: str

    @classmethod
    def from_dict(cls, d: dict) -> CausalJoinSpec:
        return cls(cause=d["cause"], effect=d["effect"])

    def to_dict(self) -> dict:
        return asdict(self)


@dataclass
class CausalLinkSpec:
    """
    User-supplied definition of one causal relationship type.

    Minimal config:
        name: ticket_closed_after_escalation
        cause_event_type: escalation_opened
        effect_event_type: ticket_closed
        premise_template: "the escalation had been resolved proactively"
        outcome_template: "the ticket would have closed earlier"
        outcome_changed: true

    Optional match_field / match_value narrow which events qualify:
        match_field: facts.priority
        match_value: "high"

    Optional max_gap_days limits cause→effect window (default: no limit).

    Optional join conditions (list of CausalJoinSpec) enforce that a shared
    identifier or field value connects the cause and effect events, so you do
    not rely on simple temporal adjacency alone.
    """

    name: str
    cause_event_type: str
    effect_event_type: str
    premise_template: str
    outcome_template: str
    outcome_changed: bool = True
    match_field: str | None = None
    match_value: Any | None = None
    max_gap_days: float | None = None
    link_field: str = "id"
    join: list[CausalJoinSpec] = field(default_factory=list)
    mechanism_aliases: list[str] = field(default_factory=list)

    @classmethod
    def from_dict(cls, d: dict) -> CausalLinkSpec:
        join_raw = d.get("join", [])
        join = [
            CausalJoinSpec.from_dict(j) if isinstance(j, dict) else CausalJoinSpec(**j)
            for j in (join_raw if isinstance(join_raw, list) else [join_raw])
        ]
        return cls(
            name=d["name"],
            cause_event_type=d["cause_event_type"],
            effect_event_type=d["effect_event_type"],
            premise_template=d["premise_template"],
            outcome_template=d["outcome_template"],
            outcome_changed=bool(d.get("outcome_changed", True)),
            match_field=d.get("match_field"),
            match_value=d.get("match_value"),
            max_gap_days=d.get("max_gap_days"),
            link_field=d.get("link_field", "id"),
            mechanism_aliases=list(d.get("mechanism_aliases", [])),
            join=join,
        )


@dataclass
class SilencePairSpec:
    """
    User-supplied definition of an expected trigger→response pair.

    When a trigger_event_type fires but response_event_type never follows
    (within max_gap_days), that's a verified absence.

    search_space_subsystems tells the scorer which subsystems the agent
    must check before concluding absence.

    search_space_selectors lets the user declare precise templates (by
    subsystem) so the expected search space is intentional rather than a
    broad subsystem sample.
    """

    trigger_event_type: str
    response_event_type: str
    search_space_subsystems: list[str] = field(default_factory=list)
    search_space_selectors: list[SearchSpaceSelector] = field(default_factory=list)
    max_gap_days: float | None = None
    match_field: str | None = None
    match_value: Any | None = None
    join: list[CausalJoinSpec] = field(default_factory=list)

    @classmethod
    def from_dict(cls, d: dict) -> SilencePairSpec:
        selectors_raw = d.get("search_space_selectors", d.get("search_space", []))
        selectors = [
            SearchSpaceSelector.from_dict(s) if isinstance(s, dict) else s
            for s in (
                selectors_raw if isinstance(selectors_raw, list) else [selectors_raw]
            )
        ]
        join_raw = d.get("join", [])
        join = [
            CausalJoinSpec.from_dict(j) if isinstance(j, dict) else CausalJoinSpec(**j)
            for j in (join_raw if isinstance(join_raw, list) else [join_raw])
        ]
        return cls(
            trigger_event_type=d["trigger_event_type"],
            response_event_type=d["response_event_type"],
            search_space_subsystems=list(d.get("search_space_subsystems", [])),
            search_space_selectors=selectors,
            max_gap_days=d.get("max_gap_days"),
            match_field=d.get("match_field"),
            match_value=d.get("match_value"),
            join=join,
        )


