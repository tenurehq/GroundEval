@dataclass
class LogEvent:
    """
    The minimal event record the framework needs.

    Users supply events as JSONL with at minimum:
        id, type, timestamp, actors, artifact_ids, facts

    Extra fields are preserved in `extras` and available to causal link
    matchers via event.extras.get("your_field").

    timestamp must be an ISO-8601 string: "2026-01-15T09:30:00"
    actors is a list of actor/user IDs who participated in this event.
    artifact_ids maps artifact type → artifact ID(s).
    facts is a free-form dict of event-specific payload.
    """

    id: str
    type: str
    timestamp: str
    actors: list[str] = field(default_factory=list)
    artifact_ids: dict[str, Any] = field(default_factory=dict)
    facts: dict[str, Any] = field(default_factory=dict)
    extras: dict[str, Any] = field(default_factory=dict)

    def ts(self) -> datetime:
        return datetime.fromisoformat(self.timestamp)

    @classmethod
    def from_dict(cls, d: dict) -> LogEvent:
        known = {"id", "type", "timestamp", "actors", "artifact_ids", "facts"}
        return cls(
            id=str(d.get("id", "")),
            type=str(d.get("type", "")),
            timestamp=str(d.get("timestamp", "")),
            actors=list(d.get("actors", [])),
            artifact_ids=dict(d.get("artifact_ids", {})),
            facts=dict(d.get("facts", {})),
            extras={k: v for k, v in d.items() if k not in known},
        )

    def to_dict(self) -> dict:
        return asdict(self)

    def resolve(self, dotted_path: str) -> Any:
        """Retrieve a value from a nested attribute path like 'facts.priority'."""
        return _get_nested(self.to_dict(), dotted_path)


