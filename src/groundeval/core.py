@dataclass
class SearchSpaceSelector:
    """
    Declares how to build a portion of the expected search space for a
    silence question.  Either `id_template` or `query_template` must be set.

    Templates may reference trigger event fields via bracket notation:
        {artifact_ids.jira}
        {facts.account_id}
        {timestamp}
    """

    subsystem: str
    id_template: str | None = None
    query_template: str | None = None

    def render(self, trigger_event: LogEvent) -> dict[str, Any]:
        """
        Returns a runtime search request dict:
          {'mode': 'id', 'value': 'ENG-42'}   or
          {'mode': 'query', 'value': 'postmortem ENG-42'}
        """
        import re

        pattern = re.compile(r"\{([^}]+)\}")

        def replacer(m: Any) -> str:
            path = m.group(1)
            val = trigger_event.resolve(path)
            return str(val) if val is not None else ""

        if self.id_template:
            return {"mode": "id", "value": pattern.sub(replacer, self.id_template)}
        if self.query_template:
            return {
                "mode": "query",
                "value": pattern.sub(replacer, self.query_template),
            }
        raise ValueError("SearchSpaceSelector needs id_template or query_template")

    def to_dict(self) -> dict:
        return asdict(self)

    @classmethod
    def from_dict(cls, d: dict) -> SearchSpaceSelector:
        return cls(
            subsystem=d["subsystem"],
            id_template=d.get("id_template"),
            query_template=d.get("query_template"),
        )


